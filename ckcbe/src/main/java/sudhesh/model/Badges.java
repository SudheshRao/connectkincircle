package sudhesh.model;

public class Badges {
	int kins;
	int stat;
	public Badges(int kins,int stat){
		super();
		this.kins = kins;
		this.stat = stat;
	}

	public int getKins() {
		return kins;
	}
	public void setKins(int kins) {
		this.kins = kins;
	}
	public int getStat() {
		return stat;
	}
	public void setStat(int stat) {
		this.stat = stat;
	}

}
